# in operator
my_dict = { 3: "three", 5: "five"}
print(3 in my_dict)

# using .values()
my_dict = { 3: "three", 5: "five"}
print('three' in my_dict.values())

# len()
my_dict = { 3: "three", 5: "five"}
print(len(my_dict))

# sorted()
my_dict = { 7: "seven", 3: "three", 5: "five", 2: "two"}
print(sorted(my_dict))
