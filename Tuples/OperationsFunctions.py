# Tuple operations / functions

myTuple = (1,4,3,2,5)
myTuple2 = (1,2,6,9,8,7)

# Concatenation
print(myTuple + myTuple2)

# * Operator
print(myTuple * 4)

# in Operator
print(3 in myTuple)

# index()
print(myTuple.index(4))

# len()
print(len(myTuple))

# max() and min()
print(max(myTuple))
print(min(myTuple))
