# Operations
print('Concatenation')
a = [1,2,3]
b = [4,5,6]
c = a + b
print(c)

print('* Operator')
a = [0,1]
a = a * 4
print(a)

# Functions
print('len() function')
a = [0,1,2,3,4,5,6]
print(len(a))

print('max() function') # Returns item with highest value
print(max(a))

print('min() function') # Returns item with lowest value
print(min(a))

print('sum() function') # Returns sum of all items
print(sum(a))
